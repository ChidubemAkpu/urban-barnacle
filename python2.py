#Turtle projects
import turtle

screen = turtle.Screen()
diagram = turtle.Turtle()
screen.bgcolor("red")
diagram.color("black")

diagram.pensize(3)
diagram.forward(50)
diagram.left(90)
diagram.forward(50)
diagram.left(90)
diagram.forward(50)
diagram.left(90)
diagram.forward(50)

screen.mainloop()






